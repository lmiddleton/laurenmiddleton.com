              <p class="caption">I am interested in life and death cycles as they pertain to the physical body, as well as human psychology and relationships. Learning to let go of people, of past loves, of places I once called home, and of beliefs about myself and others is a process that I feel deeply and consistently. Objects that mirror these patterns in the natural world, in organic matter, and in my own body serve as powerful metaphors to visually depict these processes. The act of painting is a meditation on these emotional and physical cycles of holding on and letting go, of inhaling and exhaling, and of birth and death, that give physical form and interpretation to these experiences so that they may be examined, better understood, and accepted.</p>
              <p class="caption">I use predominately red pigment, symbolic of blood, life, and passion, to paint these organic forms. Currently I am focusing on ambiguous structures that may simultaneously appear as placentas, nebulas, flowers, cardiac and vessel tissue, shell interiors, vulvas, and fruit-like forms. The ambiguity of these forms' identities allows for a multiplicity of meaning and a unification of a literal, physical experience of life and death forces (such as the female reproductive cycle or the blooming and withering of a flower), and a metaphorical, intangible experience of life and death (such as learning to let go of relationships and ways of being that no longer serve). In both cases, the synthesizing idea behind these works comes from the Jungian psychologist Clarissa Pinkola Estés' writings on what she calls the "Life/Death/Life" cycle, with which one learns to trust that the loss or diminishing of one thing will always be followed by an incoming flux of new life.</p>
              <p class="caption">My work ultimately serves to honor the Life/Death/Life force and the parts of ourselves we are all called on, again and again, to leave behind.</p>
              <p class="caption"><em>Lauren Middleton, February 2012</em></p>
